import Link from "@/components/Link";
import Tag from "@/components/Tag";
import { useState } from "react";
import Pagination from "@/components/Pagination";
import formatDate from "@/lib/utils/formatDate";
import Image from "next/image";

export default function ListLayout({
    posts,
    title,
    initialDisplayPosts = [],
    pagination,
}) {
    const [searchValue, setSearchValue] = useState("");
    const filteredBlogPosts = posts.filter((frontMatter) => {
        const searchContent =
            frontMatter.title +
            frontMatter.summary +
            frontMatter.tags.join(" ");
        return searchContent.toLowerCase().includes(searchValue.toLowerCase());
    });

    // If initialDisplayPosts exist, display it if no searchValue is specified
    const displayPosts =
        initialDisplayPosts.length > 0 && !searchValue
            ? initialDisplayPosts
            : filteredBlogPosts;

    return (
        <>
            <div className="space-y-2 md:space-y-5">
                <div className="relative">
                    <input
                        aria-label="搜索文章"
                        type="text"
                        onChange={(e) => setSearchValue(e.target.value)}
                        placeholder="搜索文章"
                        className="block w-full rounded-md border border-gray-300 bg-white px-4 py-2 text-gray-900 focus:border-primary-500 focus:ring-primary-500 dark:border-gray-900 dark:bg-gray-800 dark:text-gray-100"
                    />
                    <svg
                        className="absolute right-3 top-3 h-5 w-5 text-gray-400 dark:text-gray-300"
                        xmlns="http://www.w3.org/2000/svg"
                        fill="none"
                        viewBox="0 0 24 24"
                        stroke="currentColor"
                    >
                        <path
                            strokeLinecap="round"
                            strokeLinejoin="round"
                            strokeWidth={2}
                            d="M21 21l-6-6m2-5a7 7 0 11-14 0 7 7 0 0114 0z"
                        />
                    </svg>
                </div>
            </div>
            <ul>
                {!filteredBlogPosts.length && (
                    <p className="p-4">沒有找到文章</p>
                )}
                {displayPosts.map((frontMatter) => {
                    const { slug, date, title, summary, tags, images } =
                        frontMatter;
                    return (
                        <li key={slug} className="py-12">
                            <article>
                                <div className="space-y-2 xl:grid xl:grid-cols-4 xl:space-y-0">
                                    <dl>
                                        <dt className="sr-only">
                                            Published on
                                        </dt>
                                        <dd className="text-base font-medium leading-6 text-gray-500 dark:text-gray-400">
                                            <time dateTime={date}>
                                                {formatDate(date)}
                                            </time>
                                        </dd>
                                    </dl>
                                    <div className="flex flex-col items-center sm:flex-row xl:col-span-3">
                                        <div className="mx-2 my-8 w-full sm:my-0 sm:w-1/3">
                                            <Link href={`/blog/${slug}`}>
                                                <div className="custom-image-container">
                                                    <Image
                                                        src={images}
                                                        alt="Cover"
                                                        layout="fill"
                                                        className="custom-image"
                                                    />
                                                </div>
                                            </Link>
                                        </div>
                                        <div className="mx-2 w-full sm:w-2/3">
                                            <div className="space-y-6">
                                                <div>
                                                    <h2 className="text-2xl font-bold leading-8 tracking-tight">
                                                        <Link
                                                            href={`/blog/${slug}`}
                                                            className="text-gray-900 dark:text-gray-100"
                                                        >
                                                            {title}
                                                        </Link>
                                                    </h2>
                                                    <div className="flex flex-wrap">
                                                        {tags.map((tag) => (
                                                            <Tag
                                                                key={tag}
                                                                text={tag}
                                                            />
                                                        ))}
                                                    </div>
                                                </div>
                                                <div className="prose max-w-none text-gray-500 dark:text-gray-400">
                                                    {summary}
                                                </div>
                                            </div>
                                            <div className="text-base font-medium leading-6">
                                                <Link
                                                    href={`/blog/${slug}`}
                                                    className="text-[#ec0000] hover:text-primary-600 dark:hover:text-primary-400"
                                                    aria-label={`Read "${title}"`}
                                                >
                                                    阅读更多 &rarr;
                                                </Link>
                                            </div>
                                        </div>
                                    </div>
                                </div>
                            </article>
                        </li>
                    );
                })}
            </ul>
            {pagination && pagination.totalPages > 1 && !searchValue && (
                <Pagination
                    currentPage={pagination.currentPage}
                    totalPages={pagination.totalPages}
                />
            )}
        </>
    );
}
