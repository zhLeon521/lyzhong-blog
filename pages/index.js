import Hero from "@/components/Hero";
import Link from "@/components/Link";
import { PageSEO } from "@/components/SEO";
import Tag from "@/components/Tag";
import siteMetadata from "@/data/siteMetadata";
import { getAllFilesFrontMatter } from "@/lib/mdx";
import formatDate from "@/lib/utils/formatDate";
import Image from "next/image";

const MAX_DISPLAY = 4;

export async function getStaticProps() {
    const posts = await getAllFilesFrontMatter("blog");

    return { props: { posts } };
}

export default function Home({ posts }) {
    return (
        <>
            <PageSEO
                title={siteMetadata.title}
                description={siteMetadata.description}
            />
            <div className="divide-y divide-gray-200 dark:divide-gray-700">
                <div className="space-y-2 pt-6 pb-8 md:space-y-5">
                    <Hero />
                    <h1 className="text-3xl font-semibold leading-9 tracking-tight text-gray-900 dark:text-gray-100 sm:text-4xl sm:leading-10 md:leading-14">
                        最新文章
                    </h1>
                </div>
                <ul className="divide-y divide-gray-200 dark:divide-gray-700">
                    {!posts.length && "No posts found."}
                    {posts.slice(0, MAX_DISPLAY).map((frontMatter) => {
                        const { slug, date, title, summary, tags, images } =
                            frontMatter;
                        return (
                            <li key={slug} className="py-12">
                                <article>
                                    <div className="space-y-2 xl:grid xl:grid-cols-4 xl:space-y-0">
                                        <dl>
                                            <dt className="sr-only">
                                                Published on
                                            </dt>
                                            <dd className="text-base font-medium leading-6 text-gray-500 dark:text-gray-400">
                                                <time dateTime={date}>
                                                    {formatDate(date)}
                                                </time>
                                            </dd>
                                        </dl>
                                        <div className="flex flex-col items-center sm:flex-row xl:col-span-3">
                                            <div className="mx-2 my-8 w-full sm:my-0 sm:w-1/3">
                                                <Link href={`/blog/${slug}`}>
                                                    <div className="custom-image-container">
                                                        <Image
                                                            src={images}
                                                            alt="Cover"
                                                            layout="fill"
                                                            className="custom-image"
                                                        />
                                                    </div>
                                                </Link>
                                            </div>
                                            <div className="mx-2 w-full sm:w-2/3">
                                                <div className="space-y-6">
                                                    <div>
                                                        <h2 className="text-2xl font-bold leading-8 tracking-tight">
                                                            <Link
                                                                href={`/blog/${slug}`}
                                                                className="text-gray-900 dark:text-gray-100"
                                                            >
                                                                {title}
                                                            </Link>
                                                        </h2>
                                                        <div className="flex flex-wrap">
                                                            {tags.map((tag) => (
                                                                <Tag
                                                                    key={tag}
                                                                    text={tag}
                                                                />
                                                            ))}
                                                        </div>
                                                    </div>
                                                    <div className="prose max-w-none text-gray-500 dark:text-gray-400">
                                                        {summary}
                                                    </div>
                                                    <div className="text-base font-medium leading-6">
                                                        <Link
                                                            href={`/blog/${slug}`}
                                                            className="text-[#ec0000] hover:text-primary-600 dark:hover:text-primary-400"
                                                            aria-label={`Read "${title}"`}
                                                        >
                                                            阅读更多 &rarr;
                                                        </Link>
                                                    </div>
                                                </div>
                                            </div>
                                        </div>
                                    </div>
                                </article>
                            </li>
                        );
                    })}
                </ul>
            </div>
            {posts.length > MAX_DISPLAY && (
                <div className="flex justify-end text-base font-medium leading-6">
                    <Link
                        href="/blog"
                        className="text-[#ec0000] hover:text-primary-600 dark:hover:text-primary-400"
                        aria-label="all posts"
                    >
                        全部文章 &rarr;
                    </Link>
                </div>
            )}
        </>
    );
}
